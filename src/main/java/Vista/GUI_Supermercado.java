    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int _id;
        if (!this.txtId_Buscar.getText().equals("")) {
            _id = Integer.valueOf(this.txtId_Buscar.getText());
            Producto p = Controller.obtenerProducto(_id);

            if (p != null) {
                id = p.getId() + "";
                nombre = p.getNombre();
                cantidad = String.valueOf(p.getCantidad());
                categoria = p.getCategoria();
                precio = String.valueOf(p.getPrecio());

                this.setFields(id, nombre, cantidad, categoria, precio);

                this.btnEliminar.setEnabled(true);
                this.btnActualizar.setEnabled(false);
                this.enabledFileds(true);

                JOptionPane.showMessageDialog(null, "Busqueda exitosa...!", "Mensaje Informativo",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {

                JOptionPane.showMessageDialog(null, "No se encontró el prodcuto\ncon id = " + id, "Busqueda Fallida",
                        JOptionPane.WARNING_MESSAGE);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Debe ingresar un ID", "Error",
                        JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

