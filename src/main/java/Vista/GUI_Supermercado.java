    private void tblConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblConsultarMouseClicked
        JTable table = (JTable) evt.getSource();
        int row = table.rowAtPoint(evt.getPoint());
        DefaultTableModel model = (DefaultTableModel) this.tblConsultar.getModel();
        
        id = model.getValueAt(row, 0).toString();
        nombre = model.getValueAt(row, 1).toString();
        cantidad = model.getValueAt(row, 2).toString();
        categoria = model.getValueAt(row, 3).toString();
        precio = model.getValueAt(row, 4).toString();
        
        this.setFields(id, nombre, cantidad, categoria, precio);
        this.btnEliminar.setEnabled(true);
        this.enabledFileds(true);
        
    }//GEN-LAST:event_tblConsultarMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String id = this.txtId_Buscar.getText();
        
        if (id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese valores válidos",
                    "Verificación de campos", JOptionPane.WARNING_MESSAGE);
        }else {
            Controller.borrarProducto(Integer.valueOf(id));
            this.setFields("","","","Seleccionar...","");
            this.consultarLista();
            this.txtId_Buscar.setEnabled(true);
            this.enabledFileds(false);
            this.EnabledButtons(false);
            
            JOptionPane.showMessageDialog(null, "El producto con id= "+id+" \nfué eliminado...");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
