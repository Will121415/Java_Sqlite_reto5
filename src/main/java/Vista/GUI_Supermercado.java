    private void focusLost(JTextField field, String placeholder) {
        
        if(field.getText().equals("")) {
            System.out.println("Entro aqui: "+field.getText());
            field.setForeground(Color.gray);
            field.setText(placeholder);
       }
    }
    private void focusGained(JTextField field, String placeholder) {
       
        if (field.getText().equals(placeholder)) {
            field.setText("");
            field.setForeground(Color.black); 
        }
 
    }
