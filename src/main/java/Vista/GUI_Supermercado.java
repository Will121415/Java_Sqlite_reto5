    private void consultarLista() {
        DefaultTableModel model = (DefaultTableModel) this.tblConsultar.getModel();
        model.setRowCount(0);
        
        List<Producto> productos = Controller.listarProductos();
        
        for (int i = 0; i < productos.size(); i++) {
            Producto p = productos.get(i);
            model.insertRow(i, new Object[] {p.getId(),p.getNombre(), p.getCantidad(),p.getCategoria(),p.getPrecio()});
            
        }
    }
